SELECT DISTINCT gv.TenGV AS HoTenGV
FROM TKB tkb
JOIN GiaoVien gv ON tkb.ID_GV = gv.ID_GV
WHERE tkb.GioVao < '2025-04-15 11:00:00' 
AND tkb.GioRa > '2025-04-15 09:00:00';

