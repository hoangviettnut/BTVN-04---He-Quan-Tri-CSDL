SELECT 
    gv.TenGV AS HoTenGV,
    mh.TenMon AS MonDay,
    tkb.GioVao,
    tkb.GioRa
FROM TKB tkb
JOIN GiaoVien gv ON tkb.ID_GV = gv.ID_GV
JOIN MonHoc mh ON tkb.MaMon = mh.MaMon;

